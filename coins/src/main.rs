#[derive(Debug)]
enum UsState {
    Massachusetts,
    Ohio
}
enum Coin {
    Penny,
    Nickel,
    Dime,
    Quarter(UsState),
}

fn value_in_cents(coin: Coin) -> u32 {
    match coin {
        Coin::Penny => 1,
        Coin::Nickel => 5,
        Coin::Dime => 10,
        Coin::Quarter(state) => {
            println!("State quarter from {:?}!", state);
            25
        },
    }
}

fn main() {
    let my_coin = Coin::Quarter(UsState::Massachusetts);
    println!("{}", value_in_cents(my_coin));

}
